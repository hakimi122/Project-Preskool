        String sessionId = request.getParameter("sessionId");
        String studentId = request.getParameter("studentId");
        IAttendanceDAO ad = new AttendanceDAO();

        Attendance a = ad.getAttendance(Integer.parseInt(sessionId), Integer.parseInt(studentId));
